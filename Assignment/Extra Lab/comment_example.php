<?php
// This is a single-line comment
echo "<h3>PHP Comments Example</h3>";

/*
 This is a multi-line comment.
 You can write multiple lines of notes here.
 PHP will ignore everything inside this block.
*/
echo "This line is after a multi-line comment.<br>";

# This is another way to write a single-line comment using the # symbol
echo "This line is after a hash (#) comment.<br>";

// Example with an inline comment
$sum = 5 + 10; // Adding two numbers
echo "The sum of 5 and 10 is: " . $sum;
?>
